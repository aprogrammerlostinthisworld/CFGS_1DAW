        SALAR DECIMAL(4,0) NOT NULL,
        COMIS DECIMAL(4,0),
        NUMHI SMALLINT     NOT NULL,
        NOMEM VARCHAR(20)  NOT NULL,
		PRIMARY KEY(NUMEM));


