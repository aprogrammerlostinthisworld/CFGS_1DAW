public class Propuestos_04 {	

	public static void main(String[] args) {
		
		int num=5;
		num +=num-1*4+1;												//num+=5-(1*4)+1
		System.out.println(num);										// Resultado=7
		
		num=4;
		num %=7%3*7>>1;													//num%=((7%3)*7)>>1
		System.out.println(num);										// Resultado=1
	}
}